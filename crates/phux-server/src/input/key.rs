    /// Semantic equivalence: each wire [`KeyAction`] variant routes to the
    /// correct libghostty [`LgAction`] variant. ADR-0006 implied
    /// discriminant equality, but the live values differ (libghostty:
    /// Release=0, Press=1, Repeat=2 vs. wire: Press=0, Release=1,
    /// Repeat=2). The conversion function compensates; this test pins the
    /// semantic mapping rather than numeric equality.
    #[test]
    fn key_action_semantic_mapping_is_correct() {
        assert_eq!(key_action_to_libghostty(KeyAction::Press), LgAction::Press);
        assert_eq!(
            key_action_to_libghostty(KeyAction::Release),
            LgAction::Release
        );
        assert_eq!(
            key_action_to_libghostty(KeyAction::Repeat),
            LgAction::Repeat
        );
    }

    /// Semantic equivalence for [`ModSet`] → [`Mods`]. ADR-0006 implied
    /// bit-for-bit equality, but live layouts differ
    /// (libghostty: SHIFT=1, CTRL=2, ALT=4; wire: SHIFT=1, ALT=2, CTRL=4 —
    /// CTRL/ALT swapped, and `*_SIDE` shifts follow). `modset_to_libghostty`
    /// does the explicit remap; this test pins each flag's
    /// post-conversion identity.
    #[test]
    fn modset_semantic_round_trip() {
        let pairs: &[(ModSet, Mods)] = &[
            (ModSet::SHIFT, Mods::SHIFT),
            (ModSet::ALT, Mods::ALT),
            (ModSet::CTRL, Mods::CTRL),
            (ModSet::SUPER, Mods::SUPER),
            (ModSet::CAPS_LOCK, Mods::CAPS_LOCK),
            (ModSet::NUM_LOCK, Mods::NUM_LOCK),
            (ModSet::SHIFT_SIDE, Mods::SHIFT_SIDE),
            (ModSet::ALT_SIDE, Mods::ALT_SIDE),
            (ModSet::CTRL_SIDE, Mods::CTRL_SIDE),
            (ModSet::SUPER_SIDE, Mods::SUPER_SIDE),
        ];
        for &(wire, lg) in pairs {
            assert_eq!(
                modset_to_libghostty(wire),
                lg,
                "wire {wire:?} should map to libghostty {lg:?}",
            );
        }
        // Combined: SHIFT | CTRL on the wire becomes SHIFT | CTRL in libghostty.
        assert_eq!(
            modset_to_libghostty(ModSet::SHIFT | ModSet::CTRL),
            Mods::SHIFT | Mods::CTRL,
        );
    }
