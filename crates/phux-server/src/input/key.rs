//! Key event translation: wire → libghostty-vt.
//!
//! ADR-0006 prescribes that [`PhysicalKey`](phux_protocol::input::key::PhysicalKey)
//! / [`KeyAction`](phux_protocol::input::key::KeyAction) /
//! [`ModSet`](phux_protocol::input::key::ModSet) mirror libghostty's
//! `key::Key` / `key::Action` / `key::Mods` one-to-one. `PhysicalKey`
//! discriminants do match libghostty's exactly (177 variants, pinned by
//! [`tests::physical_key_discriminants_match_libghostty`]), but
//! `KeyAction` and `Mods` ship with different numeric layouts in the live
//! libghostty rev — see [`key_action_to_libghostty`] and
//! [`modset_to_libghostty`] for the explicit semantic remap that this
//! module performs.
//!
//! [`PerPaneKeyEncoder`] owns the per-pane `libghostty_vt::key::Encoder` plus a
//! reusable byte buffer — call [`PerPaneKeyEncoder::encode`] with the pane's
//! current [`Terminal`] and the wire event; the returned `&[u8]` is the PTY
//! payload.

use libghostty_vt::{
    Error, Terminal,
    key::{Action as LgAction, Encoder as LgKeyEncoder, Event as LgKeyEvent, Key as LgKey, Mods},
};
use phux_protocol::input::key::{KeyAction, KeyEvent, ModSet, PhysicalKey};

/// Convert a wire [`KeyAction`] to libghostty's [`LgAction`].
///
/// ADR-0006 implied discriminant equality but in the live rev libghostty
/// has Release=0, Press=1, Repeat=2 while the wire has Press=0, Release=1,
/// Repeat=2. The match arms here do the semantic remap.
#[must_use]
pub const fn key_action_to_libghostty(action: KeyAction) -> LgAction {
    match action {
        KeyAction::Press => LgAction::Press,
        KeyAction::Release => LgAction::Release,
        KeyAction::Repeat => LgAction::Repeat,
    }
}

/// Convert a wire [`ModSet`] to libghostty's [`Mods`].
///
/// ADR-0006 claims bit-for-bit equivalence, but the live bit layouts differ:
/// libghostty orders `SHIFT|CTRL|ALT|SUPER|...` while phux-protocol orders
/// `SHIFT|ALT|CTRL|SUPER|...` (`CTRL` and `ALT` are swapped, and the
/// `*_SIDE` bits follow suit). This function does the explicit semantic
/// remap so the server-side encoder sees the correct modifier set. The
/// `modset_semantic_round_trip` test pins the contract.
#[must_use]
pub fn modset_to_libghostty(mods: ModSet) -> Mods {
    let mut out = Mods::empty();
    if mods.contains(ModSet::SHIFT) {
        out |= Mods::SHIFT;
    }
    if mods.contains(ModSet::ALT) {
        out |= Mods::ALT;
    }
    if mods.contains(ModSet::CTRL) {
        out |= Mods::CTRL;
    }
    if mods.contains(ModSet::SUPER) {
        out |= Mods::SUPER;
    }
    if mods.contains(ModSet::CAPS_LOCK) {
        out |= Mods::CAPS_LOCK;
    }
    if mods.contains(ModSet::NUM_LOCK) {
        out |= Mods::NUM_LOCK;
    }
    if mods.contains(ModSet::SHIFT_SIDE) {
        out |= Mods::SHIFT_SIDE;
    }
    if mods.contains(ModSet::ALT_SIDE) {
        out |= Mods::ALT_SIDE;
    }
    if mods.contains(ModSet::CTRL_SIDE) {
        out |= Mods::CTRL_SIDE;
    }
    if mods.contains(ModSet::SUPER_SIDE) {
        out |= Mods::SUPER_SIDE;
    }
    out
}

/// Convert a wire [`PhysicalKey`] to libghostty's [`LgKey`].
///
/// Discriminants are pinned equal to libghostty's by phux-protocol's
/// `physical_key_discriminants_match_libghostty` test (mirrored here as a live
/// check). For the rare case libghostty adds a variant we haven't seen,
/// this falls back to [`LgKey::Unidentified`].
#[must_use]
#[allow(
    clippy::missing_panics_doc,
    reason = "infallible by construction — discriminants are pinned by test"
)]
pub fn physical_key_to_libghostty(key: PhysicalKey) -> LgKey {
    // PhysicalKey is `#[repr(u32)]` with discriminants matching `LgKey`.
    // `LgKey::try_from` is generated by `int_enum::IntEnum`.
    LgKey::try_from(key as u32).unwrap_or(LgKey::Unidentified)
}

/// Fallible conversion from a wire [`KeyEvent`] to libghostty's [`LgKeyEvent`].
///
/// Orphan-rules prevent us from offering this as a trait impl (`KeyEvent`
/// and `LgKeyEvent` both live outside this crate). Fallibility comes from
/// libghostty's FFI allocator — the field copy itself is total. ADR-0006
/// described this as a `From`-style conversion; this free function is the
/// realized shape.
pub fn key_event_to_libghostty(ev: &KeyEvent) -> Result<LgKeyEvent<'static>, Error> {
    let mut out = LgKeyEvent::new()?;
    out.set_action(key_action_to_libghostty(ev.action))
        .set_key(physical_key_to_libghostty(ev.key))
        .set_mods(modset_to_libghostty(ev.mods))
        .set_consumed_mods(modset_to_libghostty(ev.consumed_mods))
        .set_composing(ev.composing)
        .set_utf8(ev.text.clone());
    if let Some(cp) = ev.unshifted_codepoint
        && let Some(ch) = char::from_u32(cp)
    {
        out.set_unshifted_codepoint(ch);
    }
    Ok(out)
}

/// Per-pane key encoder.
///
/// Owns one `libghostty_vt::key::Encoder` plus a growable byte buffer reused
/// across calls. Per-pane: each pane should hold its own instance so encoder
/// state (KIP flags, modifyOtherKeys, etc.) reflects only that pane's
/// terminal state. See ADR-0006 §"Encoder options stay server-local".
#[derive(Debug)]
pub struct PerPaneKeyEncoder {
    encoder: LgKeyEncoder<'static>,
    buf: Vec<u8>,
}

impl PerPaneKeyEncoder {
    /// Construct a new per-pane key encoder with a fresh libghostty allocator.
    pub fn new() -> Result<Self, Error> {
        Ok(Self {
            encoder: LgKeyEncoder::new()?,
            buf: Vec::with_capacity(32),
        })
    }

    /// Encode a wire key event into PTY bytes.
    ///
    /// Refreshes encoder options from `terminal` (cursor key application
    /// mode, keypad mode, modifyOtherKeys, KIP flags, alt-esc-prefix,
    /// backarrow — see ADR-0006) before each encode so the bytes match
    /// what the inner program currently expects.
    ///
    /// Returns a slice borrowed from `self`'s internal buffer; the slice is
    /// valid until the next call to `encode`.
    pub fn encode(
        &mut self,
        event: &KeyEvent,
        terminal: &Terminal<'_, '_>,
    ) -> Result<&[u8], Error> {
        let lg_event = key_event_to_libghostty(event)?;
        self.encoder.set_options_from_terminal(terminal);
        self.buf.clear();
        self.encoder.encode_to_vec(&lg_event, &mut self.buf)?;
        Ok(&self.buf)
    }
}

